package com.manager.function.entity;

import java.io.Serializable;

public class GlobalLanguage implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String id;      		  //int(11)      主键ID         
	private String lan_level_content; //varchar(30)  语言难度内容   
	private String lan_level_summary; //varchar(50)  语言难度简介   
	private String create_date;       //datetime     创建时间       
	private String create_adminuser;  //varchar(30)  创建管理员账号 
	private String update_date;       //datetime     更新时间       
	private String update_adminuser;  //varchar(30)  更新管理员账号 
	
	public GlobalLanguage() {
		super();
	}
	public GlobalLanguage(String id, String lan_level_content,
			String lan_level_summary, String create_date,
			String create_adminuser, String update_date, String update_adminuser) {
		super();
		this.id = id;
		this.lan_level_content = lan_level_content;
		this.lan_level_summary = lan_level_summary;
		this.create_date = create_date;
		this.create_adminuser = create_adminuser;
		this.update_date = update_date;
		this.update_adminuser = update_adminuser;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the lan_level_content
	 */
	public String getLan_level_content() {
		return lan_level_content;
	}
	/**
	 * @param lan_level_content the lan_level_content to set
	 */
	public void setLan_level_content(String lan_level_content) {
		this.lan_level_content = lan_level_content;
	}
	/**
	 * @return the lan_level_summary
	 */
	public String getLan_level_summary() {
		return lan_level_summary;
	}
	/**
	 * @param lan_level_summary the lan_level_summary to set
	 */
	public void setLan_level_summary(String lan_level_summary) {
		this.lan_level_summary = lan_level_summary;
	}
	/**
	 * @return the create_date
	 */
	public String getCreate_date() {
		return create_date;
	}
	/**
	 * @param create_date the create_date to set
	 */
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	/**
	 * @return the create_adminuser
	 */
	public String getCreate_adminuser() {
		return create_adminuser;
	}
	/**
	 * @param create_adminuser the create_adminuser to set
	 */
	public void setCreate_adminuser(String create_adminuser) {
		this.create_adminuser = create_adminuser;
	}
	/**
	 * @return the update_date
	 */
	public String getUpdate_date() {
		return update_date;
	}
	/**
	 * @param update_date the update_date to set
	 */
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
	/**
	 * @return the update_adminuser
	 */
	public String getUpdate_adminuser() {
		return update_adminuser;
	}
	/**
	 * @param update_adminuser the update_adminuser to set
	 */
	public void setUpdate_adminuser(String update_adminuser) {
		this.update_adminuser = update_adminuser;
	}
	
	

}
