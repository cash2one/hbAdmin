package com.manager.admin.entity;

import java.io.Serializable;

public class AdminRight implements Serializable{
	private static final long serialVersionUID = 1L;
	
	/**
	 * create table TBL_ADMIN_RIGHT
		(
		  right_id    NUMBER not null,
		  right_name  NVARCHAR2(100),
		  parent_id   NUMBER,
		  right_state NCHAR(1) default '0' not null,
		  show_order  NUMBER default 0 not null,
		  right_level NUMBER default 0 not null
		)
	 */
	
	private String rightId;
	private String rightName;
	private String parentId;
	private String rightState;
	private String showOrder;
	private String rightLevel;
	private String parentName;
	public AdminRight() {
		super();
	}
	
	
	public AdminRight(String rightId, String rightName, String parentId,
			String rightState, String showOrder, String rightLevel,
			String parentName) {
		super();
		this.rightId = rightId;
		this.rightName = rightName;
		this.parentId = parentId;
		this.rightState = rightState;
		this.showOrder = showOrder;
		this.rightLevel = rightLevel;
		this.parentName = parentName;
	}


	/**
	 * @return the rightId
	 */
	public String getRightId() {
		return rightId;
	}
	/**
	 * @param rightId the rightId to set
	 */
	public void setRightId(String rightId) {
		this.rightId = rightId;
	}
	/**
	 * @return the rightName
	 */
	public String getRightName() {
		return rightName;
	}
	/**
	 * @param rightName the rightName to set
	 */
	public void setRightName(String rightName) {
		this.rightName = rightName;
	}
	/**
	 * @return the parentId
	 */
	public String getParentId() {
		return parentId;
	}
	/**
	 * @param parentId the parentId to set
	 */
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	/**
	 * @return the rightState
	 */
	public String getRightState() {
		return rightState;
	}
	/**
	 * @param rightState the rightState to set
	 */
	public void setRightState(String rightState) {
		this.rightState = rightState;
	}
	/**
	 * @return the showOrder
	 */
	public String getShowOrder() {
		return showOrder;
	}
	/**
	 * @param showOrder the showOrder to set
	 */
	public void setShowOrder(String showOrder) {
		this.showOrder = showOrder;
	}
	/**
	 * @return the rightLevel
	 */
	public String getRightLevel() {
		return rightLevel;
	}
	/**
	 * @param rightLevel the rightLevel to set
	 */
	public void setRightLevel(String rightLevel) {
		this.rightLevel = rightLevel;
	}
	/**
	 * @return the parentName
	 */
	public String getParentName() {
		return parentName;
	}
	/**
	 * @param parentName the parentName to set
	 */
	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	
	
	
}
